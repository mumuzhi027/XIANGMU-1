import turtle
t=turtle.Pen()
t.speed(10)
#设置窗口大小
t.screen.setup(600,600)
#画边框
t.penup()
t.goto(-210,0)
t.setheading(-90)
t.pendown()
t.pencolor('yellow')
t.pensize(4)
t.begin_fill()
t.circle(210,360)
t.fillcolor('yellow')
t.end_fill()
#笑容
t.pencolor('black')
t.pensize(5)
t.penup()
t.goto(-150,-30)
t.pendown()
t.setheading(-90)
t.circle(150,180)
#眼眶
t.penup()
t.forward(90)
t.setheading(155)
t.pendown()
t.pensize(40)
t.pencolor('white')
t.circle(160,45)
t.penup()
t.circle(160,-45)
t.setheading(180)
t.forward(180)
t.setheading(155)
t.pendown()
t.circle(160,45)
#点睛
t.penup()
t.setheading(0)
t.forward(20)
t.pendown()
t.pensize(16)
t.pencolor('black')
t.circle(8)
t.penup()
t.forward(180)
t.pendown()
t.circle(8)

turtle.done()